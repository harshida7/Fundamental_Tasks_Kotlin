
fun main() {

    println("Enter Value::")
    var ch:Int = readLine()!!.toInt()

    if(ch > 0)
    {
        println("$ch Is Positive")
    }

   else if(ch < 0)
    {
        println("$ch Is Negative")
    }

    else
    {
        println("$ch Is Zero")
    }

}





